// WAP to describe how primitive data types(int, float, double, characters, and Boolean) are declared and display the result

public class One {
    public static void main(String[] args) {
        int n1 = 1;
        float n2 = 5.2f;
        char ch = 's';
        double n3 = 3.05;
        boolean flag = true;
        System.out.println("Integer: " + n1);
        System.out.println("Float: " + n2);
        System.out.println("Character: " + ch);
        System.out.println("Double: " + n3);
        System.out.println("Boolean: " + flag);
    }
}
